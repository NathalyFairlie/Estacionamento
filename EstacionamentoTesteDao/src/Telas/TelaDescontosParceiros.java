    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // Obtenha os valores dos campos de texto
        String nome = txtNomeDesconto.getText();
        String descontoText = txtDesconto.getText();

        if (nome.isEmpty() && descontoText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, preencha os campos nome e desconto corretamente\n");
            return;
        } else if (nome.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, preencha corretamente o nome\n ");
            return;
        } else if (descontoText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, preencha corretamente o desconto\n ");
            return;
        }

        try {
            // Substituir ',' por '.' no campo txtDesconto
            descontoText = descontoText.replace(',', '.');

            // Verificar se o texto contém apenas números e '.' após a substituição
            if (!descontoText.matches("^\\d*\\.?\\d*$")) {
                throw new NumberFormatException();
            }

            double desconto = Double.parseDouble(descontoText);

            // Check if desconto is a non-negative value (you can modify this condition based on your requirements)
            if (desconto < 0) {
                throw new NumberFormatException();
            }

            Desconto novoDesconto = new Desconto(nome, desconto);
            daoDesconto.addDesconto(novoDesconto);
            // Atualizar o texto no campo txtDesconto
            txtDesconto.setText(descontoText);
            JOptionPane.showMessageDialog(this, "Cadastro salvo com sucesso!");
            limparCampos();

            // Atualizar a lista de itens do ComboBox
            atualizarComboBoxDescontos();

            // Adicione a instância à lista de valores (ou faça o que for necessário com esses valores)
            List<Desconto> listaDesconto = Desconto.CadastroDesconto();
            listaDesconto.add(novoDesconto);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, insira um valor numérico válido.");
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

