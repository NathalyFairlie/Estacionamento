    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
       int selectedIndex = ComboBoxDesc.getSelectedIndex();
        if (selectedIndex != -1) {
            String selectedDesconto = (String) ComboBoxDesc.getSelectedItem();
            String nomeDesconto = selectedDesconto.split(" - ")[0]; // Extrair o nome do desconto

            // Excluir o desconto do DaoDesconto
            daoDesconto.removerDesconto(nomeDesconto);

            // Atualizar a ComboBox após a exclusão
            atualizarComboBoxDescontos();

            // Limpar a seleção da ComboBox
            ComboBoxDesc.setSelectedIndex(-1);
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um desconto para excluir.");
        }
    
    }//GEN-LAST:event_btnExcluirActionPerformed

