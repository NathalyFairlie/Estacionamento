    private void atualizarTabelaPagamentos() {
        DefaultTableModel model = (DefaultTableModel) tblPagamento.getModel();
        model.setRowCount(0);

        List<Pagamentos> listaPagamentos = daoPagamentos.listaPagamento();

        for (Pagamentos pagamento : listaPagamentos) {
            Object[] rowData = {
                pagamento.getPlaca(),
                pagamento.getData(),
                pagamento.getValorPago()
            };
            model.addRow(rowData);
        }
    }

