    private void btnSalvarValoresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarValoresActionPerformed
          setCamposEditaveis(false);
    try {
        // Obtenha os valores dos campos de texto
        double hora1 = Double.parseDouble(txt1Hora.getText());
        double hora2 = Double.parseDouble(txt2Horas.getText());
        double hora3 = Double.parseDouble(txt3Horas.getText());
        double hora6 = Double.parseDouble(txt6Horas.getText());
        double hora12 = Double.parseDouble(txt12Horas.getText());
        double diaria = Double.parseDouble(txt24Horas.getText());
        double mensal = Double.parseDouble(txtMensal.getText());

        Valores novosValores = new Valores(hora1, hora2, hora3, hora6, hora12, diaria, mensal);

        List<Valores> listaValores = daoValores.listaValores();

        if (!listaValores.isEmpty()) {
            // Se houver valores salvos, atualize os valores existentes
            Valores valores = listaValores.get(0); // Supondo que só haja um conjunto de valores no arquivo
            daoValores.updateValores( novosValores);
        } else {
            // Se não houver valores salvos, adicione os novos valores
            daoValores.addValores(novosValores);
        }
        JOptionPane.showMessageDialog(this, "Cadastro salvo com sucesso!");

    } catch (NumberFormatException e) {
        // Trate a exceção se a entrada do usuário não for um número válido
        JOptionPane.showMessageDialog(this, "Por favor, insira valores numéricos válidos.");
    }


    }//GEN-LAST:event_btnSalvarValoresActionPerformed

