    private void addFocusListeners(JTextField textField) {
        textField.addFocusListener(new FocusAdapter() {
            @Override
            public void focusGained(FocusEvent e) {
                // Remove vírgulas e pontos antes de editar
                String text = textField.getText().replaceAll("[,.]", "");
                textField.setText(text);
            }

            @Override
            public void focusLost(FocusEvent e) {
                // Formata o texto para exibir duas casas decimais após o ponto
                try {
                    double value = Double.parseDouble(textField.getText().replace(",", "."));
                    String formattedValue = new DecimalFormat("#0.00", new DecimalFormatSymbols(Locale.US)).format(value);
                    textField.setText(formattedValue);
                } catch (NumberFormatException ex) {
                    // Trata a exceção se a entrada do usuário não for um número válido
                    textField.setText("0.00");
                }
            }
        });
    }

